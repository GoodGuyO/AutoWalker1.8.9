    public static void glColor(Color color){
        final float f = (color.getRGB() >> 24 & 0xFF) / 255.0f;
        final float f2 = (color.getRGB() >> 16 & 0xFF) / 255.0f;
        final float f3 = (color.getRGB() >> 8 & 0xFF) / 255.0f;
        final float f4 = (color.getRGB() & 0xFF) / 255.0f;
        //GL11.glColor4f(f2, f3, f4, f);
        GlStateManager.color(f2, f3, f4, f);
    }
    @SubscribeEvent
    public void onRender(RenderWorldLastEvent e) {
        Minecraft mc = Minecraft.getMinecraft();
        if(AutoWalker.autoWalker.currentPath==null){
            return;
        }
        for (BlockPos pos : AutoWalker.autoWalker.currentPath) {
            glColor(new Color(0, 47, 255, 128));
            drawSolidBlockESP(pos.getX() - mc.getRenderManager().viewerPosX,
                    pos.getY() - mc.getRenderManager().viewerPosY,
                    pos.getZ() - mc.getRenderManager().viewerPosZ);
        }
    }
